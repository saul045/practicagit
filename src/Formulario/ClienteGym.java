 public void consultar (){
  try{
    String id = JOptionPane.showInputDialog(null, "ingresa el id");
    String qry = " Select * from public.gym where id = ? ";
    PreparedStatement ps = con.prepareStatement(qry);
ps.setInt(1,Integer.parseInt(id));
ResultSet rs = ps.executeQuery();
int contador = 0;
while (rs.next()){
    contador ++;
    txtId.setText(rs.getString("id")); 
    txtNombreGym.setText(rs.getString("nombre"));
    txtApellidoGym.setText(rs.getString("apellido"));
    txtEdadGym.setText(rs.getString("edad"));
    txtDpiGym.setText(rs.getString("dpi"));
    txtTelefonoGym.setText(rs.getString("telefono"));
    txtCuota.setText(rs.getString("cuota"));
    txtFechaIngreso.setText(rs.getString("fecha_registro"));
    txtFechaIngreso.setEditable(false);
      String membresia = rs.getString("membresia");
      Selecion.setSelectedItem(membresia);
}
if (contador == 0){
    JOptionPane.showMessageDialog(null,"no existe cliente");
}
    }catch (SQLException ex){
        System.out.println(ex.getMessage());
        
    }
    }
