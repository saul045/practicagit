public double CalcularCuota(){
String Membresia = Selecion.getSelectedItem().toString();
            double gimnasio = 0;
            double pscina = 0;
            double zumba = 0;
            double total = 0;
                if (Membresia.equalsIgnoreCase("black")){
              gimnasio = 350;
            pscina = 200;
            total = gimnasio + pscina;
            JOptionPane.showMessageDialog(null,"gimansio: Q350.00\n"+"pscina: Q200\n"
            +"total:"+total);
          
        }else if (Membresia.equalsIgnoreCase("Smart")){
                gimnasio = 350;
            zumba = 50;
            total = gimnasio + zumba;
            JOptionPane.showMessageDialog(null,"gimansio: Q350.00\n"+"zumba: Q50\n"
            +"total:"+total);
            
        }else if (Membresia.equalsIgnoreCase("ligth")){
                gimnasio = 360.50;
            total = gimnasio;
            JOptionPane.showMessageDialog(null,"gimansio: Q360.50\n"
            +"total:"+total);
        }else {JOptionPane.showMessageDialog(null,"escriba membresia correcta");
}
            txtCuota.setText(String.valueOf(total));
            txtCuota.setEditable(false);
        return total;
}
